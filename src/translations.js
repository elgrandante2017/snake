// Función para obtener traducción
export function getTranslation(key, language = 'es') {
    return translations[language]?.[key] || translations.es[key] || key;
}

// Función para cambiar idioma de toda la interfaz
export function changeLanguage(language) {
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, language);

        if (element.tagName === 'INPUT' && element.type === 'button') {
            element.value = translation;
        } else if (element.tagName === 'BUTTON') {
            element.textContent = translation;
        } else {
            element.textContent = translation;
        }
    });

    // Actualizar elementos específicos que no tienen data-translate
    updateSpecificElements(language);

    // Actualizar atributo lang del HTML
    document.documentElement.lang = language;

    // Guardar idioma seleccionado
    localStorage.setItem('selectedLanguage', language);
}

// Función para actualizar elementos específicos
function updateSpecificElements(language) {
    const t = (key) => getTranslation(key, language);

    // Título del juego
    document.querySelector('h1').textContent = t('gameTitle');

    // Botón de reinicio/empezar
    const restartBtn = document.getElementById('restartButton');
    restartBtn.textContent = restartBtn.textContent === 'Empezar' ? t('startButton') : t('restartButton');

    // Estadísticas
    document.querySelector('.score-container').innerHTML = `${t('score')}: <span id="score">${document.getElementById('score').textContent}</span>`;
    document.querySelector('.record-container').innerHTML = `${t('record')}: <span id="record">${document.getElementById('record').textContent}</span>`;
    document.querySelector('.level-info').innerHTML = `${t('level')}: <span id="level">${document.getElementById('level').textContent}</span>`;

    // Monedas
    const coinCounter = document.getElementById('coin-counter');
    coinCounter.innerHTML = `
        <div class="coin-icon">$</div>
        <span id="coin-amount">${document.getElementById('coin-amount').textContent}</span>
    `;

    // Tienda
    const shopLabel = document.querySelector('.shop-label');
    if (shopLabel) shopLabel.textContent = t('shop');

    // Brillo
    const brightnessContent = document.querySelector('.brightness-content h3');
    if (brightnessContent) brightnessContent.textContent = t('brightness');

    const brightnessLabel = document.querySelector('.brightness-slider-container label');
    if (brightnessLabel) brightnessLabel.textContent = `${t('brightness')}:`;

    // Idiomas
    const languageContent = document.querySelector('.language-content h3');
    if (languageContent) languageContent.innerHTML = `🌐 ${t('languages')}`;
}