// Funci√≥n para obtener traducci√≥n
export function getTranslation(key, language = 'es') {
    return translations[language]?.[key] || translations.es[key] || key;
}

// Funci√≥n para cambiar idioma de toda la interfaz
export function changeLanguage(language) {
    const elements = document.querySelectorAll('[data-translate]');

    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = getTranslation(key, language);

        if (element.tagName === 'INPUT' && element.type === 'button') {
            element.value = translation;
        } else if (element.tagName === 'BUTTON') {
            element.textContent = translation;
        } else {
            element.textContent = translation;
        }
    });

    // Actualizar elementos espec√≠ficos que no tienen data-translate
    updateSpecificElements(language);

    // Actualizar atributo lang del HTML
    document.documentElement.lang = language;

    // Guardar idioma seleccionado
    localStorage.setItem('selectedLanguage', language);
}

